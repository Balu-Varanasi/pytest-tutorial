"""Examples."""


def add(a, b):
    """Add given two numbers."""
    return a + b


def subtract(a, b):
    """Subtract 'b' from 'a'."""
    return a - b


def multiply(a, b):
    """Calculate the product of given numbers."""
    return a * b


def divide(a, b):
    """Divide a by b."""
    return a / b
